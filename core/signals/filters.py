import numpy as np
try:
    from numba import njit
except ImportError:
    def njit(func): return func

from core.utils.aspects import log_dsp_action

@log_dsp_action
@njit
def moving_average_recursive(x, M):
    """
    Однородный рекурсивный фильтр (Скользящее среднее).
    Для 10 варианта M=79.
    Пояснение: Каждая точка выхода - это среднее арифметическое M точек входа.
    """
    y = np.zeros_like(x)          # Создаем пустой массив (нули) такой же длины как входной сигнал x
    current_sum = 0.0              # Переменная, где будем копить сумму чисел в "окне"
    
    # 1. Считаем сумму для самого первого окна (первые M элементов)
    for i in range(M):             # Цикл от 0 до M-1 (для 10 вар: до 78)
        current_sum += x[i]        # Суммируем первые элементы: x[0]+x[1]+...+x[78]

    y[M-1] = current_sum / M       # Записываем первое среднее значение в позицию M-1
    
    # 2. Рекурсивный алгоритм (быстрый сдвиг окна)
    for n in range(M, len(x)):     # Идем от 79-го элемента до конца сигнала
        # Чтобы не складывать 79 чисел заново, берем старую сумму, 
        # прибавляем новый элемент x[n] и вычитаем тот, что "вышел" из окна x[n-M]
        current_sum += (x[n] - x[n-M]) 
        y[n] = current_sum / M      # Новое среднее = обновленная сумма / количество элементов
    return y

@log_dsp_action
@njit
def fir_manual_filter(x, h):
    """
    Программная реализация КИХ-фильтрации (Свертка).
    x - входной зашумленный сигнал, h - коэффициенты фильтра ("шаблон").
    """
    N, M = len(x), len(h)          # N - длина сигнала (16000), M - длина фильтра (151)
    y = np.zeros(N)                 # Массив, куда запишем результат очистки
    
    for i in range(N):              # Проходим по каждой точке выходного сигнала
        s = 0.0                     # Переменная для накопления суммы (результат в одной точке)
        for j in range(M):          # Накладываем фильтр h на сигнал x
            if i - j >= 0:          # Проверка: не пытаемся ли мы взять данные до начала сигнала?
                # Основная формула фильтра: умножаем отсчет сигнала на коэффициент фильтра
                # y[i] = x[i]*h[0] + x[i-1]*h[1] + x[i-2]*h[2] + ...
                s += x[i - j] * h[j] 
        y[i] = s                    # Сохраняем итоговое взвешенное значение в точку i
    return y

@log_dsp_action
def fir_window_design(f_low, f_high, M, sr=8000, window_type='blackman'):
    """
    Расчет коэффициентов КИХ-фильтра (Метод окна).
    10 вариант: Полосовой фильтр (80-300 Гц), длина 151, окно Блэкмана.
    """
    # 1. Переводим Гц в радианы (отношение частоты к частоте дискретизации)
    w_l = 2 * np.pi * f_low / sr   # Нижняя граница (80 Гц) -> радианы
    w_h = 2 * np.pi * f_high / sr  # Верхняя граница (300 Гц) -> радианы
    
    n = np.arange(M)               # Массив индексов [0, 1, 2 ... 150]
    center = (M - 1) / 2           # Центр фильтра (75), вокруг него всё симметрично

    # Внутренняя функция: идеальный фильтр (Sinc)
    def sinc_ideal(w, n, mid):
        res = np.zeros_like(n, dtype=float)
        idx = (n != mid)           # Для всех точек, кроме центральной
        # Формула: sin(w*(n-center)) / (pi*(n-center))
        res[idx] = np.sin(w * (n[idx] - mid)) / (np.pi * (n[idx] - mid))
        res[~idx] = w / np.pi      # В самом центре (предел) значение равно w/pi
        return res

    # 2. Формируем полосовой фильтр как разность двух Низкочастотных (ФНЧ)
    # Идеальный фильтр (h_d) = ФНЧ(300Гц) - ФНЧ(80Гц)
    h_d = sinc_ideal(w_h, n, center) - sinc_ideal(w_l, n, center)

    # 3. Применяем сглаживающее окно (чтобы спектр был чистым, без "звона")
    if window_type == 'blackman':
        # Окно Блэкмана: плавно сводит края фильтра к нулю по хитрой формуле
        w_n = 0.42 - 0.5 * np.cos(2 * np.pi * n / (M - 1)) + 0.08 * np.cos(4 * np.pi * n / (M - 1))
    elif window_type == 'hamming':
        w_n = 0.54 - 0.46 * np.cos(2 * np.pi * n / (M - 1))
    else:
        w_n = np.ones(M)           # Прямоугольное окно (без сглаживания)

    return h_d * w_n               # Итоговые коэффициенты = идеальные значения * окно

@log_dsp_action
def iir_design(params, sr=8000):
    """
    Расчет коэффициентов БИХ (Резонансный полосовой фильтр).
    Для 10 варианта: f0=200 Гц, bw=60 Гц.
    """
    m_type = params.get('type')
    b, a = np.zeros(3), np.ones(3)  # b - множители для входа (x), a - для выхода (y)
    
    if m_type == 'bandpass':        # Реализация полосового резонанса
        # R - крутизна/добротность (насколько узким будет пик)
        # Зависит от ширины полосы (bw): чем шире полоса, тем меньше R
        R = 1 - 3 * (params['bw'] / sr)
        # K - положение пика (частота f0)
        K = np.cos(2 * np.pi * params['f0'] / sr)
        
        # Коэффициенты b (входная часть разностного уравнения)
        b[0] = 1 - R                # Множитель для текущего x[n]
        b[1] = 0                    # Вклад x[n-1] в этой схеме нулевой
        b[2] = -(1 - R)             # Множитель для x[n-2]
        
        # Коэффициенты a (обратная связь - прошлые выходы y)
        a[1] = -2 * R * K           # Множитель для y[n-1]
        a[2] = R**2                 # Множитель для y[n-2]
    return b, a

@log_dsp_action
@njit
def apply_iir(x, b, a):
    """
    Реализация БИХ-фильтра (Разностное уравнение).
    y[n] = b0*x[n] + b1*x[n-1] + b2*x[n-2] - a1*y[n-1] - a2*y[n-2]
    """
    y = np.zeros_like(x)            # Массив для очищенного сигнала
    for n in range(len(x)):         # Цикл по всем точкам сигнала
        # Считаем вклад текущего и прошлых входных данных (x)
        out = b[0] * x[n]           
        if n >= 1:
            out += b[1] * x[n-1]    # Добавляем вклад от x[n-1]
            out -= a[1] * y[n-1]    # ВЫЧИТАЕМ вклад прошлого выхода (Обратная связь!)
        if n >= 2:
            out += b[2] * x[n-2]    # Добавляем вклад от x[n-2]
            out -= a[2] * y[n-2]    # Вычитаем вклад позапрошлого выхода
        y[n] = out                  # Записываем итог в массив y

    # Нормировка (чтобы громкость сигнала не изменилась после фильтра)
    sum_b = np.sum(b)
    sum_a = np.sum(a)
    if abs(sum_b) > 1e-5:           # Если фильтр что-то пропускает
        gain = sum_b / (sum_a + 1e-12) # Считаем общий коэффициент усиления
        if abs(gain) > 1e-6 and abs(gain - 1.0) > 1e-3:
            return y / gain         # Делим сигнал на усиление для компенсации
    return y

def iir_bandpass(f0, bw, sr=8000):
    """ Быстрый вызов для создания полосового БИХ-фильтра """
    return iir_design({'type': 'bandpass', 'f0': f0, 'bw': bw}, sr)
