import numpy as np

def dft(x):
    """
    ЭТАП 1: РАЗБОР ЗВУКА НА ЧАСТОТЫ (Discrete Fourier Transform)
    
    Вход 'x': Наша "лента" звука (массив signal из 1024 чисел).
    """
    N = len(x)
    n = np.arange(N)      # Метки времени на ленте (0, 1, 2... 1023)
    k = n.reshape((N, 1)) # Номера частотных трафаретов (0, 1, 2... 1023)
    
    # ШАГ 1: Создаем 1024 "трафарета" (эталонных волн).
    # Каждый трафарет - это идеальная синусоида своей частоты.
    e = np.exp(-2j * np.pi * k * n / N)
    
    # ШАГ 2: Прикладываем трафареты к нашей ленте звука 'x'.
    # Мы перемножаем ленту с каждым трафаретом и складываем результат.
    # Если звук на ленте совпал с трафаретом (например, на 110 Гц) - получится большое число.
    result = np.dot(e, x)
    
    # ИТОГ: Мы получили массив из 1024 комплексных чисел.
    # В них "зашит" рецепт звука: какие частоты в нем были и какой громкости.
    return result

def idft(X):
    """
    ЭТАП 2: СБОРКА ЗВУКА ИЗ ЧАСТОТ (Inverse DFT)
    
    Вход 'X': Рецепт частот (то, что выдал dft).
    """
    N = len(X)
    n = np.arange(N)
    k = n.reshape((N, 1))
    
    # ШАГ 1: Берем наши 1024 трафарета (но теперь с обратным знаком +2j).
    e = np.exp(2j * np.pi * k * n / N)
    
    # ШАГ 2: Умножаем силу каждой частоты из рецепта на трафарет и складываем всё.
    # ШАГ 3: Делим на N (нормировка), чтобы звук не стал слишком громким.
    # ИТОГ: Мы получили назад нашу исходную "ленту" звука (1024 числа).
    return np.dot(e, X) / N

def fft(x):
    """
    БЫСТРЫЙ СПОСОБ РАЗБОРА (Fast Fourier Transform)
    
    Делает ВСЁ ТО ЖЕ САМОЕ, что и dft, но по частям.
    """
    x = np.asarray(x, dtype=complex)
    N = len(x)
    
    # База: если осталась 1 точка, её не с чем сравнивать.
    if N <= 1: return x
    
    # ШАГ 1: Разрезаем ленту на две (четные и нечетные числа).
    # Это позволяет не сравнивать всё со всем, а считать половинки отдельно.
    even = fft(x[0::2]) 
    odd =  fft(x[1::2])
    
    # ШАГ 2: Склеиваем половинки через специальный множитель (T).
    T = np.exp(-2j * np.pi * np.arange(N // 2) / N) * odd
    
    # ИТОГ: Тот же рецепт частот, что и в dft, но полученный в 100 раз быстрее.
    return np.concatenate([even + T, even - T])

def ifft(X):
    """
    БЫСТРЫЙ СПОСОБ СБОРКИ (Inverse FFT)
    """
    # Использует алгоритм быстрого сканера (fft), чтобы собрать звук назад.
    return np.conjugate(fft(np.conjugate(X))) / len(X)
